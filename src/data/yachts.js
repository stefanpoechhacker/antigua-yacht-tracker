// Generate movement deltas for each yacht (they move around realistically)
export function generateMovementDeltas(yacht) {
  const speedKnots = yacht.speed;
  const headingRad = (yacht.heading * Math.PI) / 180;
  const degreesPerKnot = 0.0003; // rough conversion
  const dt = degreesPerKnot * speedKnots;

  // Add some randomness to simulate real sailing
  const jitter = 0.0001;
  return {
    dlat: Math.cos(headingRad) * dt + (Math.random() - 0.5) * jitter,
    dlng: Math.sin(headingRad) * dt + (Math.random() - 0.5) * jitter,
    headingDrift: (Math.random() - 0.5) * 5,
    speedDrift: (Math.random() - 0.5) * 0.3,
  };
}

